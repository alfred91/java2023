package ejercicio3;

public class Direccion {
private String via;
private int numero;
private String poblacion;
private String provincia;
private float cp;
private String pais;
private double latitud, longitud;

public Direccion(String via, int numero, String poblacion, String provincia, float cp, String pais, double latitud,
		double longitud) {
	super();
	this.via = via;
	this.numero = numero;
	this.poblacion = poblacion;
	this.provincia = provincia;
	this.cp = cp;
	this.pais = pais;
	this.latitud = latitud;
	this.longitud = longitud;
}


}
