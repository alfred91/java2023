package Documentacion;
public class Calcular {
/**
 * Funcion que determina si es numero par o no
 * @param a
 * @return
 */
		public boolean esPar(int a) { 
			return a % 2 == 0;
		}
/**
 * Funcion para sumar a y b
 * @param a
 * @param b
 * @return
 */
		public int suma(int a, int b){ 
			return a+ b;
		}
	}